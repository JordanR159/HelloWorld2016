
public class Square 
{
	
	private double x;
	private double y;
	private double width;
	private double height;
	private double rotation;
	
	public double getX()
	{
		return x;
	}
	
	public double getY()
	{
		return y;
	}
	
	public double getWidth()
	{
		return width;
	}
	
	public double getHeight()
	{
		return height;
	}
	
	public double getRotation()
	{
		return rotation;
	}

}
